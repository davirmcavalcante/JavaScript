var c = 1
while (c <= 6){
    console.log(`Passo ${c}`)
    c++
}

c = 5
do {
    console.log(`Passo ${c}`)
    c++
} while (c <= 6)

/* 
O while é uma estrutura de repetição com teste lógico no início e depois executa o bloco de comandos.
while (condição){
    bloco de comandos
}

O do while é uma estrutura de repetiçao que executa o bloco de comandos primeiro e depois faz o teste lógico.
do{
    bloco de comandos
} while (condição)

Note que o teste lógico gerando a repetição ocorre até que o valor do teste seja falso.
*/
